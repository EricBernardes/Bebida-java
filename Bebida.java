public class Bebida {
    private String sabor;
    private double volume;

    public String getSabor(){
        return sabor;
    }

    public void setSabor(String novoSabor){
        sabor=novoSabor;
    }

    public double getVolume(){
        return volume;
    }

    public void setVolume(double novoVolume){
        volume=novoVolume;
    }
    
}
